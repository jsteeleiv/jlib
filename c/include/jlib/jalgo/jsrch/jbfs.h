/* Breadth-First Search
-----------------------
    Graph traversal algorithm
    explores vertices in order of their distance from source vertex
        visiting all neighbors at the present depth before moving to next level

    time complexity: O(V+E) {V=vertices, E=edges}
    space complexity: O(V)
*/


